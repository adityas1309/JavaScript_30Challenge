let arr= [1,5,10,15];
console.log("Original array:", arr);
//Use push method to add new num in end
arr.push(35);
console.log("Array after push:", arr);
//Use pop method to remove num from end
arr.pop();
console.log("Array after pop:", arr);
//Use shift method to remove the 1st element
arr.shift();
console.log("Array after shift:", arr);
//Use unshift method to add new num in beginning
arr.unshift(60);
console.log("Array after unshift:", arr);
