//Compare two numbers using > and <
let a=46,b=89;
console.log(a>b);
console.log(a<b);

//Compare two numbers using >= and <=
let c=12,d=12,e=15;
console.log(c>=d); //if equal then true or c>d then true
console.log(e<=d);

//Compare two numbers using == and ===
let s='12',g=12,z=12;
console.log(s==g); //loosely checked without checking type 
console.log(s===z); //strictly checked with checking type also
console.log(z===g); //strictly checked with checking type also
