//Use && operator
let a=true,b=false,c=true;
console.log(a&&b); //if any of the one is false then it is false
console.log(a&&c); //if both true then only true

//Use || operatoer
let x=true,y=false,z=false;
console.log(x||y); //if one is true then result is true
console.log(y||z); //if both are false then result is false

//Use ! operator
let s=true;
console.log(s); //gives original value of s
console.log(!s); // gives opposite of the value of s
